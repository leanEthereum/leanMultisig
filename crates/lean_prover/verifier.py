@dataclass
class ParsedCommitment:
    num_variables: int
    root: list[Fp]  # length DIGEST_ELEMS
    ood_points: list[EF]
    ood_answers: list[EF]

    def oods_constraints(self) -> list[SparseStatement]:
        return [
            SparseStatement.dense(expand_from_univariate(p, self.num_variables), ev)
            for p, ev in zip(self.ood_points, self.ood_answers)
        ]


def _eval_univariate(coeffs: list[EF], x: EF) -> EF:
    acc = EF.zero()
    for c in reversed(coeffs):
        acc = acc * x + c
    return acc


def verify_sumcheck(
    state: VerifierState, target: EF, n_vars: int, degree: int, pow_bits: int = 0
) -> tuple[list[EF], EF]:
    """Round-by-round: check `h(0) + h(1) == target`, grind, sample, fold. Returns (point, value)."""
    point: list[EF] = []
    for _ in range(n_vars):
        coeffs = state.next_extension_scalars_vec(degree + 1)
        s = coeffs[0]
        for c in coeffs:
            s = s + c
        if s != target:
            raise ProofError("Sumcheck identity failed: h(0) + h(1) != target")
        state.check_pow_grinding(pow_bits)
        r = state.sample()
        point.append(r)
        target = _eval_univariate(coeffs, r)
    return point, target


def combine_constraints(state: VerifierState, target: EF, constraints: list[SparseStatement]) -> tuple[EF, list[EF]]:
    gamma: EF = state.sample()
    combo = [EF.one()]
    for smt in constraints:
        for v in smt.values:
            target = target + combo[-1] * v[1]
            combo.append(combo[-1] * gamma)
    combo.pop()
    return target, combo


def verify_stir_challenges(
    state: VerifierState,
    cfg: dict,
    round_index: int,
    num_variables: int,
    folding_factor: int,
    num_queries: int,
    query_pow_bits: int,
    commitment: ParsedCommitment,
    folding_randomness: list[EF],
) -> list[SparseStatement]:
    log_height = whir_log_domain_size_at(cfg["num_variables"], cfg["log_inv_rate"], round_index) - folding_factor
    gen = two_adic_generator(log_height)
    state.check_pow_grinding(query_pow_bits)
    indices = state.sample_in_range(log_height, num_queries)

    def pack_answers(leaf: list[Fp]) -> list[EF]:
        if round_index == 0:
            return [EF.from_base(f) for f in leaf]
        return [EF(leaf[i : i + EF.DIMENSION]) for i in range(0, len(leaf), EF.DIMENSION)]

    constraints: list[SparseStatement] = []
    for idx in indices:
        op = state.next_merkle_opening()
        if not merkle_verify_path(commitment.root, log_height, idx, op.leaf_data, op.path):
            raise ProofError("Merkle verification failed")
        fold = eval_multilinear_evals(pack_answers(op.leaf_data), folding_randomness)
        ef_pt = EF.from_base(Fp(pow(int(gen.value), idx, P)))
        constraints.append(SparseStatement.dense(expand_from_univariate(ef_pt, num_variables), fold))
    return constraints


def verify_constraint_coeffs(constraint: SparseStatement, coeffs: list[EF]) -> bool:
    """Checks `point == [α, α², α⁴, …]` and `Σ coeffs[i]·α^i == value`."""
    assert constraint.selector_num_variables == 0
    alpha = constraint.point[0]
    if any(a * a != b for a, b in zip(constraint.point, constraint.point[1:])):
        return False
    univ_eval = _eval_univariate(coeffs, alpha)
    return all(univ_eval == v[1] for v in constraint.values)


def eval_constraints_poly(constraints: list[tuple[list[EF], list[SparseStatement]]], point: list[EF]) -> EF:
    value = EF.zero()
    pt = list(point)
    for round_idx, (randomness, smts) in enumerate(constraints):
        if round_idx > 0:
            pt = pt[whir_folding_factor_at_round(round_idx - 1) :]
        i = 0
        for smt in smts:
            inner_pt = pt[len(pt) - len(smt.point) :]
            common = next_mle(smt.point, inner_pt) if smt.is_next else eq_poly_outside(smt.point, inner_pt)
            sel_n = smt.selector_num_variables
            for v in smt.values:
                lagrange = EF.one()
                for j in range(sel_n):
                    bit = (v[0] >> (sel_n - 1 - j)) & 1
                    lagrange = lagrange * (pt[j] if bit else (EF.one() - pt[j]))
                value = value + lagrange * common * randomness[i]
                i += 1
        assert i == len(randomness)
    return value


def whir_verify(
    state: VerifierState,
    cfg: dict,
    parsed_commitment: ParsedCommitment,
    statement: list[SparseStatement],
) -> list[EF]:
    for s in statement:
        assert s.total_num_variables == parsed_commitment.num_variables

    n_rounds, final_sumcheck_rounds = whir_n_rounds_and_final_sumcheck(cfg["num_variables"])
    round_constraints: list[tuple[list[EF], list[SparseStatement]]] = []
    round_folding: list[list[EF]] = []
    target = EF.zero()

    def step(constraints: list[SparseStatement], n_fold: int, pow_bits: int) -> None:
        nonlocal target
        state.duplex()
        new_target, combo = combine_constraints(state, target, constraints)
        round_constraints.append((combo, constraints))
        sc_point, target = verify_sumcheck(state, new_target, n_fold, 2, pow_bits)
        round_folding.append(sc_point)

    step(
        parsed_commitment.oods_constraints() + statement,
        whir_folding_factor_at_round(0),
        cfg["starting_folding_pow_bits"],
    )

    prev_commitment = parsed_commitment
    nvars_round = cfg["num_variables"]
    for r in range(n_rounds):
        rp = cfg["rounds"][r]
        nvars_round -= whir_folding_factor_at_round(r)
        nood = rp["ood_samples"]
        new_commitment = ParsedCommitment(
            nvars_round,
            state.next_base_scalars_vec(DIGEST_ELEMS),
            state.sample_vec(nood) if nood else [],
            state.next_extension_scalars_vec(nood) if nood else [],
        )
        stir = verify_stir_challenges(
            state,
            cfg,
            r,
            nvars_round,
            whir_folding_factor_at_round(r),
            rp["num_queries"],
            rp["query_pow_bits"],
            prev_commitment,
            round_folding[-1],
        )
        step(
            new_commitment.oods_constraints() + stir,
            whir_folding_factor_at_round(r + 1),
            rp["folding_pow_bits"],
        )
        prev_commitment = new_commitment

    n_vars_final = nvars_round - whir_folding_factor_at_round(n_rounds)
    final_coeffs = state.next_extension_scalars_vec(1 << n_vars_final)
    final_stir = verify_stir_challenges(
        state,
        cfg,
        n_rounds,
        n_vars_final,
        whir_folding_factor_at_round(n_rounds),
        cfg["final_queries"],
        cfg["final_query_pow_bits"],
        prev_commitment,
        round_folding[-1],
    )
    for smt in final_stir:
        if not verify_constraint_coeffs(smt, final_coeffs):
            raise ProofError("Final STIR constraint mismatch")

    final_sc_point, final_sc_value = verify_sumcheck(state, target, final_sumcheck_rounds, 2)
    round_folding.append(final_sc_point)

    folding_flat = [r for chunk in round_folding for r in chunk]
    eval_weights = eval_constraints_poly(round_constraints, folding_flat)
    final_value = eval_multilinear_coeffs(final_coeffs, list(reversed(final_sc_point)))
    if final_sc_value != eval_weights * final_value:
        raise ProofError("WHIR final sumcheck check failed")

    return folding_flat


