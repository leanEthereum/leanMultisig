_POSEIDON_WIDTH = 16
_HALF_DIGEST_LEN = 4
_POSEIDON_PERMUTE_SHIFT, _POSEIDON_HALF_OUTPUT_SHIFT = 1 << 1, 1 << 2
_POSEIDON_HARDCODED_LEFT_4_FLAG_SHIFT, _POSEIDON_HARDCODED_LEFT_4_OFFSET_SHIFT = (
    1 << 3,
    1 << 4,
)


def _matvec_kb(mat: list[list[Fp]], state: list[EF]) -> list[EF]:
    out: list[EF] = []
    for row in mat:
        acc = EF.zero()
        for s, m in zip(state, row):
            acc = acc + s * m
        out.append(acc)
    return out


def _full_round(state: list[EF], rc1: list[Fp], rc2: list[Fp]) -> list[EF]:
    for rc in (rc1, rc2):
        state = _matvec_kb(
            _p1c()["mds_dense"],
            [(s + EF.from_base(c)) * (s + EF.from_base(c)) * (s + EF.from_base(c)) for s, c in zip(state, rc)],
        )
    return state


def _eval_poseidon1_16(folder: ConstraintFolder, cols: dict, extra_data: dict) -> None:
    """AIR for Poseidon1-16. Each `post` column commits an intermediate state, which we
    constrain against the local computation, then adopt to bound polynomial degree."""
    const = _p1c()
    state = list(cols["inputs"])
    initial = list(cols["inputs"])
    half_initial = half_final = const["half_full_rounds"] // 2

    for r in range(half_initial):
        state = _full_round(state, const["initial_constants"][2 * r], const["initial_constants"][2 * r + 1])
        for i, post in enumerate(cols["beginning_full_rounds"][r]):
            folder.assert_eq(state[i], post)
            state[i] = post

    state = [s + EF.from_base(c) for s, c in zip(state, const["sparse_first_rc"])]
    state = _matvec_kb(const["sparse_m_i"], state)

    n_partial = const["partial_rounds"]
    for r in range(n_partial):
        folder.assert_eq(state[0] * state[0] * state[0], cols["partial_rounds"][r])
        state[0] = cols["partial_rounds"][r]
        if r < n_partial - 1:
            state[0] = state[0] + const["sparse_scalar_rc"][r]
        old_s0 = state[0]
        new_s0 = EF.zero()
        for j in range(_POSEIDON_WIDTH):
            new_s0 = new_s0 + state[j] * const["sparse_first_row"][r][j]
        state[0] = new_s0
        for i in range(1, _POSEIDON_WIDTH):
            state[i] = state[i] + old_s0 * const["sparse_v"][r][i - 1]

    for r in range(half_final - 1):
        state = _full_round(state, const["final_constants"][2 * r], const["final_constants"][2 * r + 1])
        for i, post in enumerate(cols["ending_full_rounds"][r]):
            folder.assert_eq(state[i], post)
            state[i] = post

    # Last full round: compression mode adds `initial` (gated by flag_half_output for lanes 4..8);
    # permute mode (flag_permute=1) outputs raw state.
    last = 2 * (half_final - 1)
    state = _full_round(state, const["final_constants"][last], const["final_constants"][last + 1])
    flag_permute = cols["flag_permute"]
    not_permute = EF.one() - flag_permute
    compression_last4 = not_permute - cols["flag_half_output"]
    for i in range(_POSEIDON_WIDTH // 2):
        gate = not_permute if i < _HALF_DIGEST_LEN else compression_last4
        folder.assert_zero(gate * (state[i] + initial[i] - cols["outputs_left"][i]))
        folder.assert_zero(flag_permute * (state[i] - cols["outputs_left"][i]))
        folder.assert_zero(flag_permute * (state[i + _POSEIDON_WIDTH // 2] - cols["outputs_right"][i]))


def _eval_air_poseidon16(folder: ConstraintFolder, table: TableMeta, extra_data: dict) -> None:
    const = _p1c()
    flat, one, W = folder.flat, EF.one(), _POSEIDON_WIDTH
    half_initial = half_final = const["half_full_rounds"] // 2
    Fb = lambda v: EF.from_base(Fp(v))

    o = 0

    def take(n: int) -> list[EF]:
        nonlocal o
        chunk, o = flat[o : o + n], o + n
        return list(chunk)

    # fmt: off
    [flag_active, index_b, index_res, flag_half_output, flag_hardcoded_left,
     offset_hardcoded_left, eff_idx_left_first, eff_idx_left_second, flag_permute] = take(9)
    # fmt: on
    inputs = take(W)
    beginning_full_rounds = [take(W) for _ in range(half_initial)]
    partial_cols = take(const["partial_rounds"])
    ending_full_rounds = [take(W) for _ in range(half_final - 1)]
    outputs_left, outputs_right = take(W // 2), take(W // 2)

    precompile_data = (
        one
        + flag_permute * Fb(_POSEIDON_PERMUTE_SHIFT)
        + flag_half_output * Fb(_POSEIDON_HALF_OUTPUT_SHIFT)
        + flag_hardcoded_left * Fb(_POSEIDON_HARDCODED_LEFT_4_FLAG_SHIFT)
        + flag_hardcoded_left * offset_hardcoded_left * Fb(_POSEIDON_HARDCODED_LEFT_4_OFFSET_SHIFT)
    )
    not_hcl = one - flag_hardcoded_left
    index_a = eff_idx_left_second - not_hcl * Fb(_HALF_DIGEST_LEN)

    folder.assert_zero(
        _eval_virtual_bus_column(extra_data, flag_active, [precompile_data, index_a, index_b, index_res])
    )
    for f in (flag_active, flag_half_output, flag_hardcoded_left, flag_permute):
        folder.assert_bool(f)
    folder.assert_zero(flag_permute * (flag_half_output + flag_hardcoded_left))
    folder.assert_zero(flag_hardcoded_left * (offset_hardcoded_left - eff_idx_left_first))
    folder.assert_zero(not_hcl * (index_a - eff_idx_left_first))

    _eval_poseidon1_16(
        folder,
        {
            "inputs": inputs,
            "beginning_full_rounds": beginning_full_rounds,
            "partial_rounds": partial_cols,
            "ending_full_rounds": ending_full_rounds,
            "outputs_left": outputs_left,
            "outputs_right": outputs_right,
            "flag_half_output": flag_half_output,
            "flag_permute": flag_permute,
        },
        extra_data,
    )


_TABLE_SPECS: dict[str, dict] = {
    "execution": {"degree": 5, "n_constraints": 13, "n_shift": 2},
    "extension_op": {"degree": 4, "n_constraints": 33, "n_shift": 13},
    "poseidon16_compress": {"degree": 10, "n_constraints": 99, "n_shift": 0},
}


def verify_execution(
    bytecode_hash: list[Fp],
    bytecode_log_size: int,
    public_input: Sequence[Fp],
    proof: Proof,
    tables: Sequence[TableMeta],
    constants: dict,
    bytecode_multilinear: list[int],
) -> dict:
    state = VerifierState(proof)
    state.observe_scalars(list(public_input))
    state.observe_scalars(poseidon16_compress(bytecode_hash, SNARK_DOMAIN_SEP))

    dims = [int(x.value) for x in state.next_base_scalars_vec(3 + len(tables))]
    log_inv_rate, log_memory, public_input_len, *table_log_n_rows = dims
    if public_input_len != len(public_input):
        raise ProofError("InvalidProof: public_input length mismatch")
    if not MIN_WHIR_LOG_INV_RATE <= log_inv_rate <= MAX_WHIR_LOG_INV_RATE:
        raise ProofError("InvalidRate")
    if any(h < MIN_LOG_N_ROWS_PER_TABLE for h in table_log_n_rows):
        raise ProofError("InvalidProof: table too small")
    if log_memory < max(max(table_log_n_rows, default=0), bytecode_log_size):
        raise ProofError("InvalidProof: memory smaller than tables/bytecode")
    if not MIN_LOG_MEMORY_SIZE <= log_memory <= MAX_LOG_MEMORY_SIZE:
        raise ProofError("InvalidProof: log_memory out of range")
    if bytecode_log_size < MIN_BYTECODE_LOG_SIZE:
        raise ProofError("InvalidProof: bytecode too small")

    table_log_heights = {t.name: h for t, h in zip(tables, table_log_n_rows)}
    tables_by_name = {t.name: t for t in tables}
    tables_sorted = sort_tables_by_height(table_log_heights)

    # memory ≥ execution ≥ all other tables.
    if log_memory < table_log_heights["execution"] or table_log_heights["execution"] < tables_sorted[0][1]:
        raise ProofError("InvalidProof: memory or execution table size invariants broken")
    total_stacked = (
        (2 << log_memory)
        + (1 << max(bytecode_log_size, tables_sorted[0][1]))
        + sum(t.n_columns << table_log_heights[t.name] for t in tables)
    )
    stacked_n_vars = log2_ceil_usize(total_stacked)
    if stacked_n_vars > BASE_TWO_ADICITY + WHIR_INITIAL_FOLDING_FACTOR - log_inv_rate:
        raise ProofError("InvalidProof: stacked_n_vars exceeds WHIR domain bound")
    cfg = whir_config(log_inv_rate, stacked_n_vars)
    root = state.next_base_scalars_vec(DIGEST_ELEMS)
    ood_points = state.sample_vec(cfg["commitment_ood_samples"]) if cfg["commitment_ood_samples"] else []
    ood_answers = (
        state.next_extension_scalars_vec(cfg["commitment_ood_samples"]) if cfg["commitment_ood_samples"] else []
    )
    parsed_commitment = ParsedCommitment(stacked_n_vars, root, ood_points, ood_answers)

    logup_c = state.sample()
    state.duplex()
    max_bus_width = 1 + max(constants["max_precompile_bus_width"], constants["n_instruction_columns"])
    logup_alphas = state.sample_vec(log2_ceil_usize(max_bus_width))
    logup_alphas_eq = eval_eq(logup_alphas)
    logup = verify_generic_logup(
        state,
        logup_c,
        logup_alphas,
        logup_alphas_eq,
        log_memory,
        bytecode_multilinear,
        table_log_heights,
        tables_by_name,
        constants,
    )
    gkr_point = logup["gkr_point"]

    bus_beta = state.sample()
    state.duplex()
    air_alpha = state.sample()
    state.duplex()
    eta = state.sample()

    def powers(x: EF, n: int) -> list[EF]:
        out, cur = [], EF.one()
        for _ in range(n):
            out.append(cur)
            cur = cur * x
        return out

    alpha_powers = powers(air_alpha, max(_TABLE_SPECS[n]["n_constraints"] for n in tables_by_name) + 1)
    eta_powers = powers(eta, len(tables_sorted))
    extra_data = {"logup_alphas_eq_poly": logup_alphas_eq, "bus_beta": bus_beta, "c": logup_c}

    # Initial AIR sum: Σ η^t · (bus_num · sign + β · (bus_den − c)).
    initial_sum = EF.zero()
    for (name, _), eta_pow in zip(tables_sorted, eta_powers):
        sign = -EF.one() if tables_by_name[name].bus_direction == "Pull" else EF.one()
        initial_sum = initial_sum + eta_pow * (
            logup["bus_num"][name] * sign + bus_beta * (logup["bus_den"][name] - logup_c)
        )
    n_max = tables_sorted[0][1]
    sc_point, sc_value = verify_sumcheck(
        state, initial_sum, n_max, max(_TABLE_SPECS[n]["degree"] + 1 for n, _ in tables_sorted)
    )

    committed = {
        name: [(list(from_end(gkr_point, table_log_heights[name])), dict(logup["columns_values"][name]), {})]
        for name in tables_by_name
    }
    my_air_final = EF.zero()
    for (name, log_n_rows), eta_pow in zip(tables_sorted, eta_powers):
        meta, n_shift = tables_by_name[name], _TABLE_SPECS[name]["n_shift"]
        col_evals = state.next_extension_scalars_vec(meta.n_columns + n_shift)
        constraint_eval = air_constraint_eval(meta, col_evals, alpha_powers, extra_data)

        natural_pt = list(reversed(sc_point[-log_n_rows:])) if log_n_rows else []
        k_t = EF.one()
        for x in sc_point[: n_max - log_n_rows]:
            k_t = k_t * x
        my_air_final = (
            my_air_final
            + eta_pow * k_t * eq_poly_outside(from_end(gkr_point, log_n_rows), natural_pt) * constraint_eval
        )

        eq_vals = {i: col_evals[i] for i in range(meta.n_columns)}
        next_vals = {j: col_evals[meta.n_columns + j] for j in range(n_shift)}
        committed[name].append((natural_pt, eq_vals, next_vals))
    if my_air_final != sc_value:
        raise ProofError("AIR sumcheck: claimed value mismatch")

    public_memory = padd_with_zero_to_next_power_of_two(public_input)
    pm_point = state.sample_vec(log2_strict_usize(len(public_memory)))
    pm_eval = eval_multilinear_evals([EF.from_base(f) for f in public_memory], pm_point)

    mk = lambda point, values: SparseStatement(stacked_n_vars, list(point), values)
    previous = [
        mk(from_end(gkr_point, log_memory), [(0, logup["value_memory"]), (1, logup["value_memory_acc"])]),
        mk(pm_point, [(0, pm_eval)]),
        mk(
            from_end(gkr_point, bytecode_log_size),
            [((2 << log_memory) >> bytecode_log_size, logup["value_bytecode_acc"])],
        ),
    ]
    global_statements = stacked_pcs_global_statements(
        stacked_n_vars,
        log_memory,
        bytecode_log_size,
        previous,
        table_log_heights,
        committed,
        tables_by_name,
        constants,
    )
    whir_verify(state, cfg, parsed_commitment, global_statements)

    return {"log_inv_rate": log_inv_rate, "log_memory": log_memory, "stacked_n_vars": stacked_n_vars}


def poseidon_compress_slice_iv(data: Sequence[Fp]) -> list[Fp]:
    """Hash a multiple-of-8 sequence (Poseidon16/Davies-Meyer, all-zero IV)."""
    assert data and len(data) % 8 == 0
    h = [Fp(0)] * 8
    for i in range(0, len(data), 8):
        h = poseidon16_compress(h, list(data[i : i + 8]))
    return h


def main() -> int:
    import array, json
    from pathlib import Path

    vector_path = Path(__file__).resolve().parents[2] / "target" / "zkvm_test_vectors" / "proof.json"
    if not vector_path.exists():
        print(
            f"Test vector not found at {vector_path}. Generate it first with:\n"
            "    cargo test --release -p lean_prover --test dump_zkvm_vector -- --nocapture"
        )
        return 1

    print(f"Loading {vector_path.name}...")
    raw = json.loads(vector_path.read_text())

    arr = array.array("I")
    arr.frombytes((vector_path.parent / raw["bytecode_multilinear_path"]).read_bytes())
    assert len(arr) == raw["bytecode_multilinear_len"]
    bytecode_multilinear: list[int] = list(arr)

    fp_list = lambda xs: [Fp(v) for v in xs]
    public_input = fp_list(raw["public_input"])
    input_data = fp_list(raw["input_data"])
    proof = Proof(
        transcript=fp_list(raw["proof"]["transcript"]),
        merkle_openings=[
            MerkleOpening(leaf_data=fp_list(o["leaf_data"]), path=[fp_list(d) for d in o["path"]])
            for o in raw["proof"]["merkle_openings"]
        ],
    )

    if poseidon_compress_slice_iv(input_data) != public_input:
        print("FAIL: poseidon_compress_slice(input_data) doesn't match dumped public_input")
        return 1

    try:
        result = verify_execution(
            fp_list(raw["bytecode_hash"]),
            raw["bytecode_log_size"],
            public_input,
            proof,
            tables_from_json(raw["tables"]),
            raw["constants"],
            bytecode_multilinear,
        )
    except ProofError as e:
        print(f"FAIL: {e}")
        return 1

    print(f"OK: rec-aggregation proof verified ({result})")
    return 0


if __name__ == "__main__":
    import sys as _sys

    _sys.exit(main())
