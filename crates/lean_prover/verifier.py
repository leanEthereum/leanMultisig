def stacked_pcs_global_statements(
    stacked_n_vars: int,
    memory_n_vars: int,
    bytecode_n_vars: int,
    previous_statements: list[SparseStatement],
    table_log_heights: dict[str, int],
    committed_statements: dict[str, list[tuple[list[EF], dict[int, EF], dict[int, EF]]]],
    tables: dict[str, TableMeta],
    constants: dict,
) -> list[SparseStatement]:
    assert len(table_log_heights) == len(committed_statements)
    tables_sorted = sort_tables_by_height(table_log_heights)

    out = list(previous_statements)
    offset = (2 << memory_n_vars) + (1 << max(bytecode_n_vars, tables_sorted[0][1]))
    col_pc = constants["col_pc"]

    # Rust uses BTreeMap (sorted); Python dicts are insertion-ordered, sort here.
    def values_at(d: dict[int, EF], n_vars: int) -> list[tuple[int, EF]]:
        return [((offset >> n_vars) + i, v) for i, v in sorted(d.items())]

    for name, n_vars in tables_sorted:
        if name == "execution":
            # PC column: pin first row to starting_pc, last row to ending_pc.
            for idx, pc in [(0, constants["starting_pc"]), ((1 << n_vars) - 1, constants["ending_pc"])]:
                out.append(
                    SparseStatement.unique_value(
                        stacked_n_vars, offset + (col_pc << n_vars) + idx, EF.from_base(Fp(pc))
                    )
                )

        for point, eq_values, next_values in committed_statements[name]:
            if next_values:
                out.append(SparseStatement.new_next(stacked_n_vars, list(point), values_at(next_values, n_vars)))
            out.append(SparseStatement(stacked_n_vars, list(point), values_at(eq_values, n_vars)))

        offset += tables[name].n_columns << n_vars

    return out


N_VARS_TO_SEND_GKR_COEFFS = 5


def verify_gkr_quotient(state: VerifierState, n_vars: int) -> tuple[EF, list[EF], EF, EF]:
    """Layered sumcheck for `Σ nᵢ/dᵢ`. Returns `(quotient, point, claim_num, claim_den)`."""
    assert n_vars > N_VARS_TO_SEND_GKR_COEFFS

    nums = state.next_extension_scalars_vec(1 << N_VARS_TO_SEND_GKR_COEFFS)
    dens = state.next_extension_scalars_vec(1 << N_VARS_TO_SEND_GKR_COEFFS)
    quotient = EF.zero()
    for n, d in zip(nums, dens):
        quotient = quotient + n * d.inv()

    point = state.sample_vec(N_VARS_TO_SEND_GKR_COEFFS)
    claim_num = eval_multilinear_evals(nums, point)
    claim_den = eval_multilinear_evals(dens, point)

    for layer_n_vars in range(N_VARS_TO_SEND_GKR_COEFFS, n_vars):
        state.duplex()
        alpha = state.sample()
        raw_pt, sc_value = verify_sumcheck(state, claim_num + alpha * claim_den, layer_n_vars, 3)
        sc_point = list(reversed(raw_pt))
        nl, nr, dl, dr = state.next_extension_scalars_vec(4)
        if sc_value != eq_poly_outside(point, sc_point) * (alpha * dl * dr + nl * dr + nr * dl):
            raise ProofError("GKR step: postponed value mismatch")
        beta = state.sample()
        one_minus = EF.one() - beta
        claim_num = one_minus * nl + beta * nr
        claim_den = one_minus * dl + beta * dr
        point = sc_point + [beta]

    return quotient, point, claim_num, claim_den


def to_big_endian_in_field(value: int, bit_count: int) -> list[EF]:
    return [EF.one() if (value >> (bit_count - 1 - i)) & 1 else EF.zero() for i in range(bit_count)]


def from_end(seq: Sequence, n: int) -> list:
    return list(seq[len(seq) - n :]) if n else []


def mle_of_01234567_etc(point: Sequence[EF]) -> EF:
    """MLE of `f(i) = i` (big-endian) at `point`."""
    if not point:
        return EF.zero()
    e = mle_of_01234567_etc(point[1:])
    return e + point[0] * EF.from_base(Fp(1 << (len(point) - 1)))


def mle_of_zeros_then_ones(n_zeros: int, point: Sequence[EF]) -> EF:
    """MLE of `[0]*n_zeros ++ [1]*(2^len(point) − n_zeros)` at `point`."""
    n_values = 1 << len(point)
    assert n_zeros <= n_values
    if n_zeros == 0:
        return EF.one()
    if n_zeros == n_values:
        return EF.zero()
    half, tail = n_values >> 1, point[1:]
    if n_zeros < half:
        return (EF.one() - point[0]) * mle_of_zeros_then_ones(n_zeros, tail) + point[0]
    return point[0] * mle_of_zeros_then_ones(n_zeros - half, tail)


def finger_print(table: Fp, data: Sequence[EF], alphas_eq_poly: Sequence[EF]) -> EF:
    """`Σᵢ αᵢ · dataᵢ + α_last · table`."""
    assert len(alphas_eq_poly) > len(data)
    acc = EF.zero()
    for a, d in zip(alphas_eq_poly, data):
        acc = acc + a * d
    return acc + alphas_eq_poly[-1] * EF.from_base(table)


def sort_tables_by_height(table_log_heights: dict[str, int]) -> list[tuple[str, int]]:
    """Descending by height, alphabetical on ties (matches Rust `BTreeMap`)."""
    return sorted(sorted(table_log_heights.items()), key=lambda kv: -kv[1])


def eval_eq(point: Sequence[EF]) -> list[EF]:
    """Length-`2^n` evaluation table of `eq(point, ·)`."""
    out = [EF.one()]
    for p in point:
        out = [w for v in out for w in (v * (EF.one() - p), v * p)]
    return out


def verify_generic_logup(
    state: VerifierState,
    c: EF,
    alphas: list[EF],
    alphas_eq_poly: list[EF],
    log_memory: int,
    bytecode_multilinear: list[int],
    table_log_heights: dict[str, int],
    tables: dict[str, TableMeta],
    constants: dict,
) -> dict:
    """GKR-quotient + section-by-section (memory/bytecode/per-table) reconstruction."""
    n_instr_cols = constants["n_instruction_columns"]
    n_runtime_cols = constants["n_runtime_columns"]
    col_pc = constants["col_pc"]
    dom_mem = Fp(constants["logup_memory_domainsep"])
    dom_byte = Fp(constants["logup_bytecode_domainsep"])

    tables_sorted = sort_tables_by_height(table_log_heights)
    log_bytecode = log2_strict_usize(len(bytecode_multilinear) // (1 << log2_ceil_usize(n_instr_cols)))
    log_instr = log2_ceil_usize(n_instr_cols)
    log_n_cycles = table_log_heights["execution"]

    table_cols = lambda n: sum(len(vs) for _, vs in tables[n].lookups) + 1
    total_active_len = (
        (1 << log_memory)
        + max(1 << log_bytecode, 1 << tables_sorted[0][1])
        + (1 << log_n_cycles)
        + sum(table_cols(n) << h for n, h in tables_sorted)
    )
    total_gkr_n_vars = log2_ceil_usize(total_active_len)

    quotient, point_gkr, claim_num, claim_den = verify_gkr_quotient(state, total_gkr_n_vars)
    if quotient != EF.zero():
        raise ProofError("logup: GKR sum != 0")

    num, den = EF.zero(), EF.zero()

    def pref_at(offset: int, log_height: int) -> EF:
        n_missing = total_gkr_n_vars - log_height
        bits = to_big_endian_in_field(offset >> log_height, n_missing)
        return eq_poly_outside(bits, point_gkr[:n_missing])

    # Memory.
    mem_pt = from_end(point_gkr, log_memory)
    pref = pref_at(0, log_memory)
    value_memory_acc = state.next_extension_scalar()
    value_memory = state.next_extension_scalar()
    fp_mem = finger_print(dom_mem, [value_memory, mle_of_01234567_etc(mem_pt)], alphas_eq_poly)
    num = num - pref * value_memory_acc
    den = den + pref * (c - fp_mem)
    offset = 1 << log_memory

    # Bytecode (padded to the tallest table).
    log_byte_pad = max(log_bytecode, tables_sorted[0][1])
    byte_pt = from_end(point_gkr, log_bytecode)
    pref = pref_at(offset, log_bytecode)
    pref_pad = pref_at(offset, log_byte_pad)
    value_bytecode_acc = state.next_extension_scalar()
    bytecode_value = eval_mle_base_at_ef(bytecode_multilinear, list(byte_pt) + list(from_end(alphas, log_instr)))
    correction = EF.one()
    for a in alphas[: len(alphas) - log_instr]:
        correction = correction * (EF.one() - a)
    fp_byte = (
        bytecode_value * correction
        + mle_of_01234567_etc(byte_pt) * alphas_eq_poly[n_instr_cols]
        + alphas_eq_poly[-1] * EF.from_base(dom_byte)
    )
    num = num - pref * value_bytecode_acc
    den = (
        den
        + pref * (c - fp_byte)
        + pref_pad * mle_of_zeros_then_ones(1 << log_bytecode, from_end(point_gkr, log_byte_pad))
    )
    offset += 1 << log_byte_pad

    # Per-table: execution bytecode lookup + bus column + per-lookup memory reads.
    bus_num_vals: dict[str, EF] = {}
    bus_den_vals: dict[str, EF] = {}
    columns_values: dict[str, dict[int, EF]] = {}
    for name, log_n_rows in tables_sorted:
        meta = tables[name]
        table_values: dict[int, EF] = {}

        if name == "execution":
            eval_on_pc = state.next_extension_scalar()
            instr_evals = state.next_extension_scalars_vec(n_instr_cols)
            table_values[col_pc] = eval_on_pc
            table_values.update({n_runtime_cols + i: e for i, e in enumerate(instr_evals)})
            pref = pref_at(offset, log_n_rows)
            fp = finger_print(dom_byte, list(instr_evals) + [eval_on_pc], alphas_eq_poly)
            num = num + pref
            den = den + pref * (c - fp)
            offset += 1 << log_n_rows

        eval_on_selector = state.next_extension_scalar()
        eval_on_data = state.next_extension_scalar()
        pref = pref_at(offset, log_n_rows)
        num = num + pref * eval_on_selector
        den = den + pref * eval_on_data
        bus_num_vals[name] = eval_on_selector
        bus_den_vals[name] = eval_on_data
        offset += 1 << log_n_rows

        for index_col, value_cols in meta.lookups:
            index_eval = state.next_extension_scalar()
            assert index_col not in table_values
            table_values[index_col] = index_eval
            for i, col_index in enumerate(value_cols):
                value_eval = state.next_extension_scalar()
                assert col_index not in table_values
                table_values[col_index] = value_eval
                pref = pref_at(offset, log_n_rows)
                fp = finger_print(
                    dom_mem,
                    [value_eval, index_eval + EF.from_base(Fp(i))],
                    alphas_eq_poly,
                )
                num = num + pref
                den = den + pref * (c - fp)
                offset += 1 << log_n_rows

        columns_values[name] = table_values

    den = den + mle_of_zeros_then_ones(offset, point_gkr)
    if num != claim_num:
        raise ProofError("logup: numerators value mismatch")
    if den != claim_den:
        raise ProofError("logup: denominators value mismatch")

    return {
        "value_memory": value_memory,
        "value_memory_acc": value_memory_acc,
        "value_bytecode_acc": value_bytecode_acc,
        "bus_num": bus_num_vals,
        "bus_den": bus_den_vals,
        "gkr_point": list(point_gkr),
        "columns_values": columns_values,
    }


class ConstraintFolder:
    """`flat`/`shift` = current-row / next-row column evals. Each `assert_zero(x)`
    adds `alpha_powers[i]·x` to the accumulator."""

    def __init__(self, flat: Sequence[EF], shift: Sequence[EF], alpha_powers: Sequence[EF]) -> None:
        self.flat, self.shift, self.alpha_powers = (
            list(flat),
            list(shift),
            list(alpha_powers),
        )
        self.accumulator: EF = EF.zero()
        self.i = 0

    def assert_zero(self, x: EF) -> None:
        self.accumulator = self.accumulator + self.alpha_powers[self.i] * x
        self.i += 1

    def assert_eq(self, x: EF, y: EF) -> None:
        self.assert_zero(x - y)

    def assert_bool(self, x: EF) -> None:
        self.assert_zero(x * (EF.one() - x))


def _eval_virtual_bus_column(extra_data: dict, flag: EF, data: Sequence[EF]) -> EF:
    """`(Σ αᵢ·dataᵢ + α_last·DOMAINSEP)·β + flag` (LOGUP_PRECOMPILE_DOMAINSEP = 1)."""
    alphas: list[EF] = extra_data["logup_alphas_eq_poly"]
    bus_beta: EF = extra_data["bus_beta"]
    assert len(data) < len(alphas)
    inner = EF.zero()
    for a, d in zip(alphas, data):
        inner = inner + a * d
    inner = inner + alphas[-1] * EF.from_base(Fp(1))
    return inner * bus_beta + flag


def air_constraint_eval(
    table: TableMeta,
    col_evals: Sequence[EF],
    alpha_powers: Sequence[EF],
    extra_data: dict,
) -> EF:
    folder = ConstraintFolder(col_evals[: table.n_columns], col_evals[table.n_columns :], alpha_powers)
    {
        "execution": _eval_air_execution,
        "extension_op": _eval_air_extension_op,
        "poseidon16_compress": _eval_air_poseidon16,
    }[table.name](folder, table, extra_data)
    return folder.accumulator


def _eval_air_execution(folder: ConstraintFolder, table: TableMeta, extra_data: dict) -> None:
    # fmt: off
    (pc, fp, addr_a, addr_b, addr_c, value_a, value_b, value_c,
     operand_a, operand_b, operand_c, flag_a, flag_b, flag_c, flag_c_fp,
     flag_ab_fp, mul, jump, aux, precompile_data) = folder.flat[:20]
    # fmt: on
    pc_shift, fp_shift = folder.shift[0], folder.shift[1]
    one = EF.one()

    # nu_x = flag·operand + (1 − flag − flag_ab_fp)·value + flag_ab_fp·(fp + operand)
    nfa = -(flag_a + flag_ab_fp - one)
    nfb = -(flag_b + flag_ab_fp - one)
    nfc = -(flag_c + flag_c_fp - one)
    nu_a = flag_a * operand_a + nfa * value_a + flag_ab_fp * (fp + operand_a)
    nu_b = flag_b * operand_b + nfb * value_b + flag_ab_fp * (fp + operand_b)
    nu_c = flag_c * operand_c + nfc * value_c + flag_c_fp * (fp + operand_c)

    # aux ∈ {0,1,2}: 0=nothing, 1=add, 2=deref.
    add = aux * EF.from_base(Fp(2)) - aux * aux
    deref = aux * (aux - one) * EF.from_base(_INV_TWO)
    is_precompile = -(add + mul + deref + jump - one)

    az = folder.assert_zero
    az(_eval_virtual_bus_column(extra_data, is_precompile, [precompile_data, nu_a, nu_b, nu_c]))
    az(nfa * (addr_a - (fp + operand_a)))
    az(nfb * (addr_b - (fp + operand_b)))
    az(nfc * (addr_c - (fp + operand_c)))
    az(add * (nu_b - (nu_a + nu_c)))
    az(mul * (nu_b - nu_a * nu_c))
    az(deref * (addr_b - (value_a + operand_b)))
    az(deref * (value_b - nu_c))
    jc = jump * nu_a
    az(jc * (nu_a - one))
    az(jc * (pc_shift - nu_b))
    az(jc * (fp_shift - nu_c))
    not_jc = -(jc - one)
    az(not_jc * (pc_shift - (pc + one)))
    az(not_jc * (fp_shift - fp))


# extension_op/air.rs precompile-data layout.
_EXT_OP_FLAG_IS_BE = 4
_EXT_OP_FLAG_ADD = 8
_EXT_OP_FLAG_MUL = 16
_EXT_OP_FLAG_POLY_EQ = 32
_EXT_OP_LEN_MULTIPLIER = 64


def _quintic_mul_ef(a: Sequence[EF], b: Sequence[EF]) -> list[EF]:
    """Port of `quintic_mul` (multiplication of two EF⁵ as quintic-extension elements)."""
    assert len(a) == 5 and len(b) == 5
    b0m3, b1m4, b4m2 = b[0] - b[3], b[1] - b[4], b[4] - b[2]
    rows = [
        [b[0], b[4], b[3], b[2], b1m4],
        [b[1], b[0], b[4], b[3], b[2]],
        [b[2], b1m4, b0m3, b4m2, b[3] - b1m4],
        [b[3], b[2], b1m4, b0m3, b4m2],
        [b[4], b[3], b[2], b1m4, b0m3],
    ]

    def dot(row: list[EF]) -> EF:
        acc = a[0] * row[0]
        for i in range(1, 5):
            acc = acc + a[i] * row[i]
        return acc

    return [dot(row) for row in rows]


def _eval_air_extension_op(folder: ConstraintFolder, table: TableMeta, extra_data: dict) -> None:
    # Layout: shift columns 0..13 = (is_be, start, len, flag_{add,mul,poly_eq},
    # idx_{a,b}, comp[0..5]); then idx_res, va, vb, vres (5 each).
    f = folder.flat
    is_be, start, len_col, flag_add, flag_mul, flag_poly_eq, idx_a, idx_b = f[:8]
    comp, idx_res = f[8:13], f[13]
    va, vb, vres = f[14:19], f[19:24], f[24:29]
    s = folder.shift
    is_be_sh, start_sh, len_sh, flag_add_sh, flag_mul_sh, flag_poly_eq_sh, idx_a_sh, idx_b_sh = s[:8]
    comp_sh = s[8:13]
    one, zero = EF.one(), EF.zero()
    Fb = lambda v: EF.from_base(Fp(v))

    aux = (
        is_be * Fb(_EXT_OP_FLAG_IS_BE)
        + flag_add * Fb(_EXT_OP_FLAG_ADD)
        + flag_mul * Fb(_EXT_OP_FLAG_MUL)
        + flag_poly_eq * Fb(_EXT_OP_FLAG_POLY_EQ)
        + len_col * Fb(_EXT_OP_LEN_MULTIPLIER)
    )
    folder.assert_zero(
        _eval_virtual_bus_column(extra_data, start * (flag_add + flag_mul + flag_poly_eq), [aux, idx_a, idx_b, idx_res])
    )

    for x in (is_be, start, flag_add, flag_mul, flag_poly_eq):
        folder.assert_bool(x)

    is_ee, not_start_sh = -(is_be - one), -(start_sh - one)
    va_x = [va[0]] + [va[k] * is_ee for k in range(1, 5)]
    comp_tail = [comp_sh[k] * not_start_sh for k in range(5)]
    va_vb = _quintic_mul_ef(va_x, vb)

    for k in range(5):
        folder.assert_zero((comp[k] - (va_x[k] + vb[k] + comp_tail[k])) * flag_add)
    for k in range(5):
        folder.assert_zero((comp[k] - (va_vb[k] + comp_tail[k])) * flag_mul)

    # poly_eq: comp ← (2·va·vb − va − vb + 1) · comp_sh_or_one.
    poly_eq_val = [va_vb[k] + va_vb[k] - va_x[k] - vb[k] + (one if k == 0 else zero) for k in range(5)]
    comp_sh_or_one = [comp_sh[0] * not_start_sh + start_sh] + [comp_sh[k] * not_start_sh for k in range(1, 5)]
    poly_eq_result = _quintic_mul_ef(poly_eq_val, comp_sh_or_one)
    for k in range(5):
        folder.assert_zero((comp[k] - poly_eq_result[k]) * flag_poly_eq)
    for k in range(5):
        folder.assert_zero((comp[k] - vres[k]) * start)

    for x, y in [
        (len_col, len_sh + one),
        (is_be, is_be_sh),
        (flag_add, flag_add_sh),
        (flag_mul, flag_mul_sh),
        (flag_poly_eq, flag_poly_eq_sh),
    ]:
        folder.assert_zero(not_start_sh * (x - y))

    folder.assert_zero(not_start_sh * (idx_a_sh - idx_a - (is_be + is_ee * Fb(5))))
    folder.assert_zero(not_start_sh * (idx_b_sh - idx_b - Fb(5)))
    folder.assert_zero(start_sh * (len_col - one))


