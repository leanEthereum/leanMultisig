class EF:
    """Quintic extension `Fp[X] / (X⁵ + X² − 1)`."""

    __slots__ = ("c",)
    DIMENSION = 5

    def __init__(self, coeffs: Sequence[Fp]):
        assert len(coeffs) == 5
        self.c = tuple(coeffs)

    @staticmethod
    def zero() -> "EF":
        return EF([Fp(0)] * 5)

    @staticmethod
    def one() -> "EF":
        return EF([Fp(1)] + [Fp(0)] * 4)

    @staticmethod
    def from_base(x: Fp) -> "EF":
        return EF([x] + [Fp(0)] * 4)

    def __add__(self, o):
        if isinstance(o, Fp):
            return EF([self.c[0] + o, *self.c[1:]])
        return EF([a + b for a, b in zip(self.c, o.c)])

    def __sub__(self, o):
        if isinstance(o, Fp):
            return EF([self.c[0] - o, *self.c[1:]])
        return EF([a - b for a, b in zip(self.c, o.c)])

    def __neg__(self):
        return EF([-a for a in self.c])

    __radd__ = __add__

    def __mul__(self, o):
        if isinstance(o, Fp):
            return EF([a * o for a in self.c])
        a, b = self.c, o.c
        prod = [Fp(0)] * 9
        for i in range(5):
            for j in range(5):
                prod[i + j] = prod[i + j] + a[i] * b[j]
        for k in range(8, 4, -1):  # X^k = X^(k-5)·(1 − X²) for k ≥ 5.
            coef = prod[k]
            prod[k - 5] = prod[k - 5] + coef
            prod[k - 3] = prod[k - 3] - coef
        return EF(prod[:5])

    __rmul__ = __mul__

    def __eq__(self, o):
        return isinstance(o, EF) and self.c == o.c

    def __hash__(self):
        return hash(self.c)

    def __repr__(self):
        return f"EF({[int(x.value) for x in self.c]})"

    def inv(self) -> "EF":
        result, base, n = EF.one(), self, P**5 - 2
        while n > 0:
            if n & 1:
                result = result * base
            base = base * base
            n >>= 1
        return result


_POSEIDON16 = Poseidon1(PARAMS_16)


def poseidon16_compress_in_place(state: list[Fp]) -> list[Fp]:
    assert len(state) == WIDTH
    return [a + b for a, b in zip(_POSEIDON16.permute(state), state)]


def poseidon16_compress(left: Sequence[Fp], right: Sequence[Fp]) -> list[Fp]:
    return poseidon16_compress_in_place(list(left) + list(right))[:DIGEST_ELEMS]


def hash_slice(data: Sequence[Fp]) -> list[Fp]:
    assert len(data) % RATE == 0 and len(data) >= WIDTH
    state = poseidon16_compress_in_place(list(data[-WIDTH:]))
    for k in range(len(data) // RATE - 3, -1, -1):
        state = poseidon16_compress_in_place(state[:CAPACITY] + list(data[k * RATE : (k + 1) * RATE]))
    return state[:DIGEST_ELEMS]


class Challenger:
    """Duplex-sponge Fiat-Shamir. `observe(chunk)` permutes `state[:CAPACITY] || chunk`;
    `_sample_rate()` reads `state[CAPACITY:]` once per `duplex()`."""

    def __init__(self) -> None:
        self.state: list[Fp] = [Fp(0)] * WIDTH
        self.rate_fresh: bool = False

    def observe(self, chunk: Sequence[Fp]) -> None:
        assert len(chunk) == RATE
        self.state = _POSEIDON16.permute(self.state[:CAPACITY] + list(chunk))
        self.rate_fresh = True

    def observe_many(self, scalars: Sequence[Fp]) -> None:
        for i in range(0, len(scalars), RATE):
            chunk = list(scalars[i : i + RATE])
            chunk += [Fp(0)] * (RATE - len(chunk))
            self.observe(chunk)

    def duplex(self) -> None:
        self.observe([Fp(0)] * RATE)

    def _sample_rate(self) -> list[Fp]:
        assert self.rate_fresh, "stale rate — insert duplex() before sampling"
        self.rate_fresh = False
        return list(self.state[CAPACITY:])

    def _sample_many(self, n: int) -> list[Fp]:
        if n == 0:
            return []
        out = self._sample_rate()
        for _ in range(1, n):
            self.duplex()
            out.extend(self._sample_rate())
        return out

    def sample_vec(self, n: int) -> list[EF]:
        flat = self._sample_many((n * EF.DIMENSION + RATE - 1) // RATE)[: n * EF.DIMENSION]
        return [EF(flat[i : i + EF.DIMENSION]) for i in range(0, len(flat), EF.DIMENSION)]

    def sample(self) -> EF:
        return self.sample_vec(1)[0]

    def sample_in_range(self, bits: int, n_samples: int) -> list[int]:
        assert bits < 31
        flat = self._sample_many((n_samples + RATE - 1) // RATE)[:n_samples]
        return [int(x.value) & ((1 << bits) - 1) for x in flat]


@dataclass
class MerkleOpening:
    leaf_data: list[Fp]
    path: list[list[Fp]]


@dataclass
class Proof:
    transcript: list[Fp]
    merkle_openings: list[MerkleOpening]


class VerifierState(Challenger):
    """Challenger + transcript reader. `n` scalars are consumed as `next_multiple_of(n, RATE)`
    raw scalars (trailing must be zero) and absorbed."""

    def __init__(self, proof: Proof) -> None:
        super().__init__()
        self.transcript = list(proof.transcript)
        self.openings = list(proof.merkle_openings)
        self.offset = 0
        self.open_idx = 0

    def _read_padded(self, n: int) -> list[Fp]:
        n_pad = -(-n // RATE) * RATE
        if self.offset + n_pad > len(self.transcript):
            raise ProofError("ExceededTranscript")
        chunk = self.transcript[self.offset : self.offset + n_pad]
        self.offset += n_pad
        if any(int(chunk[i].value) for i in range(n, n_pad)):
            raise ProofError("InvalidTranscript: non-zero padding")
        self.observe_many(chunk)
        return chunk

    def observe_scalars(self, scalars: Sequence[Fp]) -> None:
        self.observe_many(list(scalars))

    def next_base_scalars_vec(self, n: int) -> list[Fp]:
        return self._read_padded(n)[:n]

    def next_extension_scalars_vec(self, n: int) -> list[EF]:
        flat = self.next_base_scalars_vec(n * EF.DIMENSION)
        return [EF(flat[i : i + EF.DIMENSION]) for i in range(0, len(flat), EF.DIMENSION)]

    def next_extension_scalar(self) -> EF:
        return self.next_extension_scalars_vec(1)[0]

    def next_merkle_opening(self) -> MerkleOpening:
        if self.open_idx >= len(self.openings):
            raise ProofError("ExceededTranscript: no more Merkle openings")
        self.open_idx += 1
        return self.openings[self.open_idx - 1]

    def check_pow_grinding(self, bits: int) -> None:
        if bits == 0:
            return
        self._read_padded(1)
        if int(self.state[CAPACITY].value) & ((1 << bits) - 1) != 0:
            raise ProofError("InvalidGrindingWitness")


_INV_TWO = Fp(pow(2, P - 2, P))


def log2_ceil_usize(x: int) -> int:
    return 0 if x <= 1 else (x - 1).bit_length()


def log2_strict_usize(x: int) -> int:
    assert x > 0 and (x & (x - 1)) == 0, f"{x} is not a power of two"
    return x.bit_length() - 1


def padd_with_zero_to_next_power_of_two(values: Sequence[Fp]) -> list[Fp]:
    if not values:
        return [Fp(0)]
    n = 1 << log2_ceil_usize(len(values))
    return list(values) + [Fp(0)] * (n - len(values))


def merkle_verify_path(
    commit: list[Fp],
    log_height: int,
    index: int,
    opened_values: Sequence[Fp],
    opening_proof: Sequence[list[Fp]],
) -> bool:
    if len(opening_proof) != log_height:
        return False
    cur = hash_slice(list(opened_values))
    for sibling in opening_proof:
        cur = poseidon16_compress(cur, sibling) if index & 1 == 0 else poseidon16_compress(sibling, cur)
        index >>= 1
    return list(commit) == list(cur)


def expand_from_univariate(x: EF, num_variables: int) -> list[EF]:
    """`[x, x², x⁴, …, x^(2^(n−1))]`."""
    out, cur = [], x
    for _ in range(num_variables):
        out.append(cur)
        cur = cur * cur
    return out


def eq_poly_outside(a: Sequence[EF], b: Sequence[EF]) -> EF:
    """`Π (1 − a_i − b_i + 2·a_i·b_i)`."""
    assert len(a) == len(b)
    one, acc = EF.one(), EF.one()
    for x, y in zip(a, b):
        xy = x * y
        acc = acc * (one + xy + xy - x - y)
    return acc


def next_mle(x: Sequence[EF], y: Sequence[EF]) -> EF:
    """Multilinear extension of `y = x + 1` (big-endian, mod `2^n`)."""
    assert len(x) == len(y)
    one = EF.one()
    n = len(x)
    eq_prefix = [one]
    for i in range(n):
        eq_prefix.append(eq_prefix[i] * (x[i] * y[i] + (one - x[i]) * (one - y[i])))
    low_suffix = [one] * (n + 1)
    for i in range(n - 1, -1, -1):
        low_suffix[i] = low_suffix[i + 1] * x[i] * (one - y[i])
    s = EF.zero()
    for i in range(n):
        s = s + eq_prefix[i] * (one - x[i]) * y[i] * low_suffix[i + 1]
    prod = one
    for v in list(x) + list(y):
        prod = prod * v
    return s + prod


def eval_multilinear_evals(evals: Sequence[EF], point: Sequence[EF]) -> EF:
    """Evaluate a multilinear in evaluation form at `point` (big-endian fold)."""
    assert len(evals) == 1 << len(point)
    cur = list(evals)
    for r in reversed(point):
        cur = [cur[j] + (cur[j + 1] - cur[j]) * r for j in range(0, len(cur), 2)]
    return cur[0]


def eval_mle_base_at_ef(base_evals: Sequence[int], point: Sequence[EF]) -> EF:
    """numpy-backed fold for the 2²²-entry bytecode multilinear."""
    import numpy as np

    assert len(base_evals) == 1 << len(point)
    pt = [tuple(int(ci.value) for ci in p.c) for p in point]
    cur = np.asarray(base_evals, dtype=np.int64) % P
    # First round: base → EF. a + (b-a)·r with r ∈ EF.
    a, b = cur[0::2], cur[1::2]
    d = (b - a) % P
    r = pt[-1]
    cur = np.stack(
        [(a + d * r[0]) % P, *[(d * r[k]) % P for k in range(1, 5)]],
        axis=1,
    )
    # EF × EF rounds: schoolbook product reduced mod X⁵+X²−1.
    for r0, r1, r2, r3, r4 in (pt[i] for i in range(len(pt) - 2, -1, -1)):
        a, b = cur[0::2], cur[1::2]
        d = (b - a) % P
        d0, d1, d2, d3, d4 = d[:, 0], d[:, 1], d[:, 2], d[:, 3], d[:, 4]
        m = lambda x, y: (x * y) % P
        p0 = m(d0, r0)
        p1 = (m(d0, r1) + m(d1, r0)) % P
        p2 = (m(d0, r2) + m(d1, r1) + m(d2, r0)) % P
        p3 = (m(d0, r3) + m(d1, r2) + m(d2, r1) + m(d3, r0)) % P
        p4 = (m(d0, r4) + m(d1, r3) + m(d2, r2) + m(d3, r1) + m(d4, r0)) % P
        p5 = (m(d1, r4) + m(d2, r3) + m(d3, r2) + m(d4, r1)) % P
        p6 = (m(d2, r4) + m(d3, r3) + m(d4, r2)) % P
        p7 = (m(d3, r4) + m(d4, r3)) % P
        p8 = m(d4, r4)
        cur = np.stack(
            [
                (a[:, 0] + p0 + p5 - p8) % P,
                (a[:, 1] + p1 + p6) % P,
                (a[:, 2] + p2 - p5 + p7 + p8) % P,
                (a[:, 3] + p3 - p6 + p8) % P,
                (a[:, 4] + p4 - p7) % P,
            ],
            axis=1,
        )
    return EF([Fp(int(v)) for v in cur[0]])


def eval_multilinear_coeffs(coeffs: Sequence[EF], point: Sequence[EF]) -> EF:
    """`Σ_b c_b · Π_i x_i^(b_i)` in the standard multilinear coefficient basis."""
    assert len(coeffs) == 1 << len(point)
    if not point:
        return coeffs[0]
    half = len(coeffs) // 2
    lo = eval_multilinear_coeffs(coeffs[:half], point[1:])
    hi = eval_multilinear_coeffs(coeffs[half:], point[1:])
    return lo + hi * point[0]


@dataclass
class SparseStatement:
    """Claim with multilinear `point` and `(selector, value)` pairs. `is_next`
    swaps the eq-weight for `next_mle`."""

    total_num_variables: int
    point: list[EF]
    values: list[tuple[int, EF]]
    is_next: bool = False

    @property
    def selector_num_variables(self) -> int:
        return self.total_num_variables - len(self.point)

    @staticmethod
    def dense(point: list[EF], value: EF) -> "SparseStatement":
        return SparseStatement(len(point), point, [(0, value)])

    @staticmethod
    def unique_value(total: int, index: int, value: EF) -> "SparseStatement":
        return SparseStatement(total, [], [(index, value)])

    @staticmethod
    def new_next(total: int, point: list[EF], values: list[tuple[int, EF]]) -> "SparseStatement":
        return SparseStatement(total, point, values, is_next=True)


def whir_folding_factor_at_round(r: int) -> int:
    return WHIR_INITIAL_FOLDING_FACTOR if r == 0 else WHIR_SUBSEQUENT_FOLDING_FACTOR


def whir_n_rounds_and_final_sumcheck(num_variables: int) -> tuple[int, int]:
    nv = num_variables - WHIR_INITIAL_FOLDING_FACTOR
    if nv < MAX_NUM_VARIABLES_TO_SEND_COEFFS:
        return 0, nv
    n = -(-(nv - MAX_NUM_VARIABLES_TO_SEND_COEFFS) // WHIR_SUBSEQUENT_FOLDING_FACTOR)
    return n, nv - n * WHIR_SUBSEQUENT_FOLDING_FACTOR


def whir_log_domain_size_at(num_variables: int, start_rate: int, r: int) -> int:
    return num_variables + start_rate - (RS_DOMAIN_INITIAL_REDUCTION_FACTOR + r - 1 if r >= 1 else 0)


